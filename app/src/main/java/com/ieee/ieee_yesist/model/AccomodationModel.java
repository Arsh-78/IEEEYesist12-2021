package com.ieee.ieee_yesist.model;

public class AccomodationModel {
    String image;
    String name;
    String address;
    String goLink;

    public AccomodationModel(){}

    public String getImage() {
        return image;
    }

    public String getName() {
        return name;
    }

    public String getAddress() {
        return address;
    }

    public String getGoLink() {
        return goLink;
    }
}
